// Using the JS syntax to simulate a struct from C++
class AirplaneStruct {
    // Constructor
    constructor(planeNum, gateNum, duration, originCity, destCity, departTime, arrivalTime) {
        this.planeNumber = planeNum;
        this.gateNumber = gateNum;
        this.duration = duration;
        this.originCity = originCity;
        this.destinationCity = destCity;
        this.departureTime = departTime;
        this.arrivalTime = arrivalTime;
    }
}